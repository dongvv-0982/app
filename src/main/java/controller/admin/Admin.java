    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        ControlDAO db = new ControlDAO();
        ArrayList<Report> reports = db.getAllReport();
        request.setAttribute("reports", reports);
        User user = db.getUser((String)request.getSession().getAttribute("user"));
        request.setAttribute("user", user);
        for (String online : onlines) {
            System.out.println(online);
        }
        request.getRequestDispatcher("../View/admin.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int approve = -1, id = 0;
        String report = request.getParameter("report");
        try{
            approve = Integer.parseInt(request.getParameter("approve"));
            id = Integer.parseInt(request.getParameter("id"));
           
        } catch(NumberFormatException ex){
            response.sendError(1005,"Invalid parameter");
            
            return;
        }
        ControlDAO db = new ControlDAO();
        if(approve == 1) {
            int tweet_id = db.getTweetId(id);
            db.deleteReport(id);
            db.deletePost(tweet_id,report);
        } else if (approve == 0){
            db.deleteReport(id);
        }
        response.sendRedirect("../admin/home");
    }

